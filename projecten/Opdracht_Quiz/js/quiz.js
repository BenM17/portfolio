//nieuw object aanmaken
function Speler(naam, score, datum) {
    this.naam = naam
    this.score = score
    this.datum = datum
}

//score/naam/datum opslagen
function addToLeaderboard(naam, score, datum) {
    //lege array voor naam/datum/score in op te slaan
    let arrayVanSpelers = [];

    //volgende elementen ophalen van localstorage
    naam = localStorage.getItem("naam");
    score = localStorage.getItem("score");
    datum = localStorage.getItem("datum");

    //indien de array leeg is, speler toevoegen
    if (localStorage.getItem("quiz_scorebord_" + quiz) == null) {
        arrayVanSpelers.push(new Speler(naam, score, datum));
    }
    else {
        //als de array niet leeg is, de string die in de array zit omzetten naar het object 'speler'
        arrayVanSpelers = JSON.parse(localStorage.getItem("quiz_scorebord_" + quiz));
        //als er meer dan 5 zijn, de oudste verwijderen
        if (arrayVanSpelers.length > 4) {
            arrayVanSpelers.shift()
        }
        //toevoegen aan die array       
        arrayVanSpelers.push(new Speler(naam, score, datum));
    }

    // wegschrijven van array in localstorage
    localStorage.setItem("quiz_scorebord_" + quiz, JSON.stringify(arrayVanSpelers));
    console.log(arrayVanSpelers);
}

//de table schrijven met de juiste quiz keuze/score
function buildTable() {

    let LegeArray = "<tr><td></td><td></td><td></td><td></td></tr>";
    for (let j = 1; j < 4; j++) {
        if (localStorage.getItem("quiz_scorebord_" + j) == null) {
            document.getElementById('myTable_' + [j]).innerHTML += LegeArray;
        }
        if (localStorage.getItem("quiz_scorebord_" + j) != null) {
            mijnArray = JSON.parse(localStorage.getItem("quiz_scorebord_" + j));


            for (let i = 0; i < mijnArray.length; i++) {
                let html = "<tr><td></td><td>" + mijnArray[i].naam + "</td><td>" + mijnArray[i].score + "/10 </td><td>" + mijnArray[i].datum + "</td></tr>";
                if (mijnArray[i].score == 10) {
                    html = "<tr><td>" + '<i class="fas fa-trophy text-warning"></i>' + "</td><td>" + mijnArray[i].naam + "</td><td>" + mijnArray[i].score + '/10 </td><td>' + mijnArray[i].datum + "</td></tr>";
                }
                if (mijnArray[i].score == 9) {
                    html = "<tr><td>" + '<i class="fas fa-medal text-muted"></i>' + "</td><td>" + mijnArray[i].naam + "</td><td>" + mijnArray[i].score + '/10 </td><td>' + mijnArray[i].datum + "</td></tr>";
                }
                if (mijnArray[i].score == 8) {
                    html = "<tr><td>" + '<i class="fas fa-award text-danger"></i>' + "</td><td>" + mijnArray[i].naam + "</td><td>" + mijnArray[i].score + '/10 </td><td>' + mijnArray[i].datum + "</td></tr>";
                }
                document.getElementById('myTable_' + [j]).innerHTML += html;
            }
        }
    }
}












